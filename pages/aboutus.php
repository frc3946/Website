<h1 class="text-center">About Us</h1>

<p>
	Slidell High School's Tiger Robotics is a student-run organization that is open to everyone who wants to join.
	The team represents high school students of all backgrounds and genders.
</p>
<p>
	Our goal: to overcome the sterotypical cliques of the standard high school setting and draw everyone together as a family,
	resulting in newfound self-esteem and positive outlets for students.
</p>
<p>
	Our fun, close-knit community teaches the participants:
</p>

<ul>
	<li>leadership</li>
	<li>teamwork</li>
	<li>gracious professionalism</li>
	<li>and character-building skills</li>
</ul>

<p>
	We strive to inspire not only our peers, but the entire Northshore region as well.
</p>
<p>
	Local industrial and technologically-focused industries, such as Textron and the Stennis Space Center, open our team to many unique partnership opportunities.
</p>
<p>
	The 2012-2013 season will be our second year competing in the FIRST Robotics Challenge.
</p>
<p>
	Tiger Robotics is a 501(c)3 non-profit organization.
</p>
